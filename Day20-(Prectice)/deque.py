from collections import deque
dq = deque([1,2,3])
dq.appendleft(0)
print(dq)
dq.append(4)
dq.pop()
print(dq)
dq.popleft()
print(dq)
dq.rotate(2)
print(dq)
dq.rotate(-1)
print(dq)
dq.rotate(3)
print(dq)
dq.extend([5,6,7])
print(dq)
dq.extendleft([-1,-2,-3])   
print(dq)
reversed(dq)
print(dq)
dq.remove(5)
print(dq)
dq.clear()
print(dq)